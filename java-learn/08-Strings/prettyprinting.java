public class prettyprinting {
    public static void main(String[] args) {
        float a= 453.1234f;
        System.out.printf("formatted number is %.2f",a);//the 2f rounds off to two decimal values
        System.out.print("\n");
        System.out.printf("pie:%.3f",Math.PI);
        System.out.print("\n");
        System.out.printf("hello my name is %s and I am %s","vamsi","cool");//place holders 
    }
    
}
